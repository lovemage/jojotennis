// 工具函式
export const filterByCity = (city) => COURTS.filter(c => c.city === city);
export const searchCourts = (keyword) =>
  COURTS.filter(c =>
    c.name.includes(keyword) || c.city.includes(keyword) ||
    c.district.includes(keyword) || c.tags.some(t => t.includes(keyword))
  );
export const filterCourts = ({ city, surface, light, indoor } = {}) =>
  COURTS.filter(c => {
    if (city && c.city !== city) return false;
    if (surface && c.surface !== surface) return false;
    if (light === true && !c.light) return false;
    if (indoor === true && !c.indoor) return false;
    return true;
  });
